namespace RestFoundation
{
    /// <summary>
    /// Defines HTTP response header names.
    /// </summary>
    public sealed class ResponseHeaderNames
    {
        /// <summary>
        /// Accept-Ranges
        /// </summary>
        public readonly string AcceptRanges = "Accept-Ranges";

        /// <summary>
        /// Content-Length
        /// </summary>
        public readonly string ContentLength = "Content-Length";

        /// <summary>
        /// Content-Range
        /// </summary>
        public readonly string ContentRange = "Content-Range";

        /// <summary>
        /// Content-Type
        /// </summary>
        public readonly string ContentType = "Content-Type";

        /// <summary>
        /// Content-Disposition
        /// </summary>
        public readonly string ContentDisposition = "Content-Disposition";

        /// <summary>
        /// Content-Encoding
        /// </summary>
        public readonly string ContentEncoding = "Content-Encoding";

        /// <summary>
        /// Content-Language
        /// </summary>
        public readonly string ContentLanguage = "Content-Language";

        /// <summary>
        /// Content-Location
        /// </summary>
        public readonly string ContentLocation = "Content-Location";

        /// <summary>
        /// ETag
        /// </summary>
        public readonly string ETag = "ETag";

        /// <summary>
        /// Location
        /// </summary>
        public readonly string Location = "Location";

        /// <summary>
        /// Warning
        /// </summary>
        public readonly string Warning = "Warning";
    }
}

        /// Access-Control-Max-Age
        /// </summary>
        public readonly string AccessControlMaxAge = "Access-Control-Max-Age";

        /// <summary>
        /// Allow
        /// </summary>
        public readonly string Allow = "Allow";

        /// <summary>
        /// Cache-Control
        /// </summary>
        public readonly string CacheControl = "Cache-Control";

